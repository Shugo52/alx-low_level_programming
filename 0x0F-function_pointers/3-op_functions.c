#include "3-calc.h"
/**
 * op_add - adds two numbers
 *
 * @a: integer a
 * @b: integer b
 * Return: result
 */
int op_add(int a, int b)
{
	return ((a) + (b));
}

/**
 * op_sub - subtract integers
 *
 * @a: integer a
 * @b: integer b
 * Return: result
 */
int op_sub(int a, int b)
{
	return ((a) - (b));
}

/**
 * op_mul - multiplies two integers
 *
 * @a: integer a
 * @b: integer b
 * Return: result
 */
int op_mul(int a, int b)
{
	return ((a) * (b));
}

/**
 * op_div - divides two integers
 *
 * @a: integer a
 * @b: integer b
 * Return: result
 */
int op_div(int a, int b)
{
	return ((a) / (b));
}

/**
 * op_mod - modulus of two integers
 *
 * @a: integer a
 * @b: integer b
 * Return: result
 */
int op_mod(int a, int b)
{
	return ((a) % (b));
}
